# Echappement de caractères
# L'utilisation du \ juste avant le retour à la ligne a permis de l'ignorer
"coucou je suis \
un test"

# \n est un retour à la ligne, en écrivant \n nous avons donc inséré un retour à la ligne dans notre chaîne
"coucou je suis \n un test"

# Single quoted
'Je suis \n un test'
